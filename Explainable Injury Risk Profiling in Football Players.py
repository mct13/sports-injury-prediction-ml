def generate_local_risk_profile(player_index, model_name, X_test, shap_values_class1,
                                explainer, y_test, y_proba):
    """Generate a comprehensive Local Risk Profile for an individual athlete."""

    player_data = X_test.iloc[player_index]
    player_shap = shap_values_class1[player_index]
    actual_label = y_test.iloc[player_index]
    predicted_proba = y_proba[player_index]
    predicted_label = 1 if predicted_proba > 0.5 else 0

    print(f"\n{'='*70}")
    print(f"LOCAL RISK PROFILE - Player Index: {player_index}")
    print(f"{'='*70}")
    print(f"Model: {model_name}")
    print(f"Predicted Injury Probability: {predicted_proba:.1%}")
    print(f"Predicted Risk Level: {'HIGH RISK' if predicted_label == 1 else 'LOW RISK ✓'}")
    print(f"Actual Outcome: {'INJURY' if actual_label == 1 else 'NO INJURY'}")

    # Sort features by absolute SHAP value (impact)
    feature_impacts = pd.DataFrame({
        'Feature': X_test.columns,
        'SHAP_Value': player_shap,
        'Feature_Value': player_data.values,
        'Abs_Impact': np.abs(player_shap)
    }).sort_values('Abs_Impact', ascending=False)

    print(f"\nTop 5 Risk Drivers (Push/Pull Analysis):")
    print(f"{'Feature':<25} {'Value':<12} {'Impact':<12} {'Direction'}")
    print("-" * 70)

    for _, row in feature_impacts.head(5).iterrows():
        direction = "↑ INCREASES RISK" if row['SHAP_Value'] > 0 else "↓ DECREASES RISK"
        print(f"{row['Feature']:<25} {row['Feature_Value']:<12.2f} {row['SHAP_Value']:<+12.4f} {direction}")

    # Generate SHAP force plot
    plt.figure(figsize=(16, 8)) # Increased height
    shap.force_plot(
        base_value, # Use the already determined scalar base_value
        player_shap,
        player_data,
        matplotlib=True,
        show=False,
        text_rotation=45 # Added text rotation
    )
    plt.title(f'Local Risk Profile - Player {player_index}\nPredicted Probability: {predicted_proba:.1%} (Actual: {'INJURY' if actual_label == 1 else 'NO INJURY'})',
    fontsize=12, fontweight='bold')
    plt.tight_layout()

    label_clean = 'high_risk' if predicted_label == 1 else 'low_risk'
    plt.savefig(f'local_risk_profile_{label_clean}_player_{player_index}.png', dpi=300, bbox_inches='tight')
    print(f"\n Local Risk Profile saved to: local_risk_profile_{label_clean}_player_{player_index}.png")
    plt.show()

    return feature_impacts

